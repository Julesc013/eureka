def _fixture_live_state(record: Mapping[str, Any]) -> str:
    status = str(record.get("status", ""))
    capabilities = _mapping(record.get("capabilities"))
    live_access = _mapping(record.get("live_access"))
    if status == "placeholder":
        return "Planning placeholder only; no connector or live access."
    if status == "local_private_future":
        return "Future local/private source only; no current local-files ingestion."
    if capabilities.get("recorded_fixture_backed"):
        return "Recorded fixtures only; no live network access."
    if capabilities.get("fixture_backed"):
        return "Committed local fixture only; no live network access."
    return str(live_access.get("notes", "No live state is claimed."))


def _capabilities_summary(record: Mapping[str, Any]) -> str:
    capabilities = _mapping(record.get("capabilities"))
    labels = [
        ("supports_search", "search"),
        ("supports_item_metadata", "item metadata"),
        ("supports_file_listing", "file listing"),
        ("supports_member_listing", "member listing"),
        ("supports_content_text", "content text"),
        ("supports_hashes", "hashes"),
        ("supports_action_paths", "action paths"),
        ("supports_temporal_captures", "temporal captures"),
        ("supports_live_probe", "live probes"),
    ]
    supported = [label for key, label in labels if capabilities.get(key)]
    if not supported:
        return "No current search, metadata, file listing, member listing, or live probe capability."
    return ", ".join(supported).capitalize() + "."


def _limitations_summary(record: Mapping[str, Any]) -> str:
    coverage = _mapping(record.get("coverage"))
    limitations = coverage.get("current_limitations")
    if isinstance(limitations, list) and limitations:
        return "; ".join(str(item) for item in limitations)
    return str(coverage.get("coverage_notes", "Fixture and placeholder boundaries remain governed."))


def _mapping(value: Any) -> Mapping[str, Any]:
    return value if isinstance(value, Mapping) else {}


def _load_templates() -> dict[str, str]:
    return {
        "base": (TEMPLATES_DIR / "base.html").read_text(encoding="utf-8"),
        "page": (TEMPLATES_DIR / "page.html").read_text(encoding="utf-8"),
    }


def _load_json(path: Path) -> Any:
    try:
        return json.loads(path.read_text(encoding="utf-8"))
    except FileNotFoundError as exc:
        raise BuildError(f"missing JSON source: {_display_path(path)}") from exc
    except json.JSONDecodeError as exc:
        raise BuildError(f"invalid JSON source {_display_path(path)}: {exc}") from exc


def _clean_output_dir(output_dir: Path) -> None:
    if not output_dir.exists():
        return
    default_output = DEFAULT_OUTPUT.resolve()
    is_default = output_dir.resolve() == default_output
    marker_exists = (output_dir / MARKER_FILE).exists()
    is_empty = output_dir.is_dir() and not any(output_dir.iterdir())
    if not (is_default or marker_exists or is_empty):
        raise BuildError(
            f"refusing to clean non-generated output without {MARKER_FILE}: {_display_path(output_dir)}"
        )
    if output_dir.is_dir():
        shutil.rmtree(output_dir)
    else:
        output_dir.unlink()


