def _format_plain(report: Mapping[str, Any]) -> str:
    lines = [
        "Static site generator",
        f"status: {report['status']}",
        f"source_root: {report.get('source_root', 'site')}",
    ]
    if report.get("output_dir"):
        lines.append(f"output_dir: {report['output_dir']}")
    if report.get("deploy_artifact_current"):
        lines.append(f"deploy_artifact_current: {report['deploy_artifact_current']}")
    if report.get("pages"):
        lines.append(f"pages: {len(report['pages'])}")
    if report.get("errors"):
        lines.append("")
        lines.append("Errors")
        lines.extend(f"- {error}" for error in report["errors"])
    return "\n".join(lines) + "\n"


def _display_path(path: Path) -> str:
    try:
        return str(path.resolve().relative_to(REPO_ROOT)).replace("\\", "/")
    except ValueError:
        return str(path)


